# Fundamental Data Types
# int and float

# we use the function type to return the data type

# int: integer
print(type(6))
print(type(2-4))
print(type(2*4))
print(type(0))

# float: number with decimal point
print(type(2/4))
print(type(20+1.1))
print(type(9.1+1.9)) # it still returns float because the sum returns 11.0

# Double multiplication: Power of
print(2**3)

# Double slash: integer division operator
print(5//4)

# module: remainder of the division
print(5%4)