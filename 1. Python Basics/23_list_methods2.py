# List Methods 2
basket = ['a','b','c','d','e']

# index
print(basket.index('c')) # returns index of the element
print(basket.index('c', 1, 4)) # You can also specify the start and end to search

# in
print('x' in basket) # Returns boolean from the condition

# count
print(basket.count('d')) # Returns number of times the element appears
 